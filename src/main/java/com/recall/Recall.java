public class Recall{

    private String manufacturer;
    private String nhtsaCampaignNumber;
    private String reportRecievedDate;
    private String component;
    private String summary;
    private String conequence;
    private String remedy;
    private String notes;
    private String modelYear;
    private String make;
    private String model;

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getNhtsaCampaignNumber() {
        return nhtsaCampaignNumber;
    }

    public void setNhtsaCampaignNumber(String nhtsaCampaignNumber) {
        this.nhtsaCampaignNumber = nhtsaCampaignNumber;
    }

    public String getReportRecievedDate() {
        return reportRecievedDate;
    }

    public void setReportRecievedDate(String reportRecievedDate) {
        this.reportRecievedDate = reportRecievedDate;
    }

    public String getComponent() {
        return component;
    }

    public void setComponent(String component) {
        this.component = component;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getConequence() {
        return conequence;
    }

    public void setConequence(String conequence) {
        this.conequence = conequence;
    }

    public String getRemedy() {
        return remedy;
    }

    public void setRemedy(String remedy) {
        this.remedy = remedy;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getModelYear() {
        return modelYear;
    }

    public void setModelYear(String modelYear) {
        this.modelYear = modelYear;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}